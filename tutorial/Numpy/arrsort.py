import numpy as np

arr = np.array([3, 2, 0, 1])

print(np.sort(arr))
#Sort the array alphabetically:


arr = np.array(['banana', 'cherry', 'apple'])

print(np.sort(arr))
#Sort a boolean array:

arr = np.array([True, False, True])

print(np.sort(arr))

#Sort a 2-D array:



arr = np.array([[3, 2, 4], [5, 0, 1]])

print(np.sort(arr))

